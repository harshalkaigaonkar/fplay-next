import {Schema, model, models} from 'mongoose';
import { MongooseSongTypes } from 'types';

const Song = new Schema<MongooseSongTypes>({
 saavn_id: {
  type: String,
  required: true,
  unique: true
 },
 name: {
  type: String,
  required: true,
  unique: true,
 },
 image: [{
  quality: String,
  link: String,
 }],
 song_urls: [{
  quality: String,
  link: String
 }],
 primary_artists: {
  type: String,
  required: true,
 },
 duration: {
  type: String,
  required: true,
 }
},
{
 timestamps: true
});

export default models.song || model<MongooseSongTypes>('song', Song)